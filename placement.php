	  <h1 style="color:blue;">Placement Cell:-</h1>
	  <p style="font-family:calibri;font-size:15px;">Training and Placement are integral parts of the BGSBU educational programmes. For this purpose the University has been 
maintaining full-fledged Placement Cell with whole time Placement Officer. The Cell organizes training activities round the year, coordinates with
 the corporate sector, prepares prospective students and facilitates Campus selection. Realizing the role the Placement 
 Cell has been playing in making our product employable, the University is planning to expand and convert it into Centre 
 for Employability Skills Development, with the long term aim of raising it into Skills Capital of Jammu & Kashmir.
 To be employable a fresh pass out must possess requisite knowledge in the area of his specialization, necessary skills 
 and aptitude (KSA). While knowledge is imparted through formal class room teaching-learning process, skills and aptitude
 are honed by professionals in skills development. The budding professionals are also helped to become clear, correct and 
 confident in their communication.
 </p><br>
 <b style="font-family:calibri;font-size:15px;color:red;">Key activities undertaken by the Centre:-</b>
 <p style="font-family:calibri;font-size:15px;">
i. Organize Personality Enhancement Workshops, group discussions, mock interviews and other 
activities that help students prepare for corporate sector. <br>
ii. Provide career consultancy to students of non-professional streams. <br>
iii. Organize regular visits of students to industrial houses as part of Campus to Corporate programme. <br>
iv. Arrange Campus recruitment for summer training which provides firsthand knowledge about corporate world to 
students and prepares them for final placements. <br>
v. Assist students in getting-on-the campus pre placements by inviting national and multinational companies 
for on campus interaction and interviews. <br>
vi. The recruiters of 
our product include leading organizations like Barkley bank, IDBI, Fortis,ICICI, Kodak Mahindra, IBM, Perot Systems, JPH Technologies, Global InfoTech etc 
        </p>
